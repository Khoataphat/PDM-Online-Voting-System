    private void jButton15ActionPerformed(java.awt.event.ActionEvent evt) {
        String serverName = "LAPTOP-O6MDECFV\\SQLEXPRESS";
        String databaseName = "Online-Voting1";
        String url = "jdbc:sqlserver://" + serverName + ":1433;databaseName=" + databaseName + ";encrypt=true;trustServerCertificate=true;";

        String electionId = jTextField1.getText();
        String electionName = jTextField2.getText();
        String startDate = jTextField7.getText();
        String endDate = jTextField6.getText();

        // Check if any required fields are empty
        if (electionId.isEmpty() || electionName.isEmpty() || startDate.isEmpty() || endDate.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all the required fields");
        } else {
            try {
                Connection con = DriverManager.getConnection(url, "sa", "123456789");
                System.out.println("Connected To SQL Server Database!");

                String sql = "INSERT INTO Election VALUES (?, ?, ?, ?, null)";
                PreparedStatement pst = con.prepareStatement(sql);

                pst.setString(1, electionId); // ID
                pst.setString(2, electionName); // Name
                pst.setTimestamp(3, Timestamp.valueOf(startDate)); // Start time
                pst.setTimestamp(4, Timestamp.valueOf(endDate)); // End time

                pst.executeUpdate();
                JOptionPane.showMessageDialog(this, "New Election Added");

                CreateElectionAddCanditates c = new CreateElectionAddCanditates(electionId);
                c.setVisible(true);

                dispose();

            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }
    }


